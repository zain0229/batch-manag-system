    public SSearch_result(int x) {
        initComponents();
        id=x;
        try{
          Connection c=DriverManager.getConnection("jdbc:mysql://localhost:3306/hgpi_batches?zeroDateTimeBehavior=convertToNull","root","");
            PreparedStatement p=c.prepareStatement("select rtrim(sid),rtrim(sname),rtrim(crsname),rtrim(doa),rtrim(phno),rtrim(email),rtrim(s.bid),rtrim(bname),rtrim(fname),rtrim(starttime),rtrim(meridian),rtrim(endtime),rtrim(meridian_to) from students s, batches b,courses c,faculty f where s.bid=b.bid and s.crsid=c.crsid and b.facid=f.fid and s.sid=?");
            p.setInt(1,id);
            ResultSet rs=p.executeQuery();
            while(rs.next())
            {
                String sid=Integer.toString(rs.getInt(1));
                String snm=rs.getString(2);
                String cnm=rs.getString(3);
                String doa=rs.getString(4);
                String phno=rs.getString(5);
                String em=rs.getString(6);
                lstssr1.add(sid+"       "+snm+"        "+cnm+"        "+doa+"         "+phno+"         "+em);
                String bid=Integer.toString(rs.getInt(7));
                String bname=rs.getString(8);
                String fn=rs.getString(9);
                String st=Integer.toString(rs.getInt(10));
                String mer=rs.getString(11);
                String et=Integer.toString(rs.getInt(12));
                String mert=rs.getString(13);
                lstssr2.add(bid+"        "+bname+"        "+fn+"         "+st+"        "+mer+"         "+et+"         "+mert);

            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }

